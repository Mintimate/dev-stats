#!/usr/bin/env node
import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import process from 'node:process';
import { fileURLToPath } from 'node:url';

const rootDir = path.resolve(fileURLToPath(new URL('..', import.meta.url)));
const skillDir = path.join(rootDir, 'skills', 'dev-stats-agent');
const outputPath = path.join(rootDir, 'agents', 'chat', '_skill.ts');
const checkOnly = process.argv.includes('--check');

const references = [
  ['Platform And Tool Routing', 'references/platform-routing.md'],
  ['Structured Profile Output', 'references/profile-output.md'],
  ['Stats Card Recipes', 'references/card-recipes.md'],
];

function readRequired(relativePath, { stripFrontmatter = false } = {}) {
  const fullPath = path.join(skillDir, relativePath);
  if (!existsSync(fullPath)) throw new Error(`Missing required skill file: ${path.relative(rootDir, fullPath)}`);
  const text = readFileSync(fullPath, 'utf8').replace(/\r\n/g, '\n').trim();
  return stripFrontmatter ? text.replace(/^---\n[\s\S]*?\n---\n?/, '').trim() : text;
}

function constantName(title) {
  return `REFERENCE_${title.toUpperCase().replace(/[^A-Z0-9]+/g, '_').replace(/^_|_$/g, '')}`;
}

function tsString(value) {
  return JSON.stringify(value, null, 2);
}

const skillBody = readRequired('SKILL.md', { stripFrontmatter: true });
const referenceBodies = references.map(([title, relativePath]) => ({
  title,
  name: constantName(title),
  body: readRequired(relativePath),
}));

const source = `// This file is generated by scripts/sync-dev-stats-agent-skill.mjs.
// Edit skills/dev-stats-agent/, then run npm run sync:agent-skill.

export const DEV_STATS_AGENT_SKILL_NAME = 'dev-stats-agent';

const SKILL_BODY = ${tsString(skillBody)};

${referenceBodies.map((reference) => `const ${reference.name} = ${tsString(reference.body)};`).join('\n\n')}

function section(title: string, body: string): string {
  return [\`## \${title}\`, body.trim()].join('\\n');
}

export function buildSkillInstructions(): string {
  return [
    'Skill: dev-stats-agent.',
    'Use this project skill as the source of truth for Stats Agent behavior.',
    section('Skill Body', SKILL_BODY),
    ${referenceBodies.map((reference) => `section(${tsString(reference.title)}, ${reference.name})`).join(',\n    ')},
  ].join('\\n\\n');
}
`;

const current = existsSync(outputPath) ? readFileSync(outputPath, 'utf8') : '';
if (current === source) {
  console.log(`${path.relative(rootDir, outputPath)} is already in sync`);
} else if (checkOnly) {
  console.error(`${path.relative(rootDir, outputPath)} is stale. Run npm run sync:agent-skill.`);
  process.exitCode = 1;
} else {
  writeFileSync(outputPath, source, 'utf8');
  console.log(`Synced ${path.relative(rootDir, outputPath)} from skills/dev-stats-agent`);
}
